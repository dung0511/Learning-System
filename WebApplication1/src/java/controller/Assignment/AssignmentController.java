    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        response.setContentType("text/html;charset=UTF-8");
        try {
            String indexPage = request.getParameter("index");
            if (indexPage == null) {
                indexPage = "1";
            }
            int index = Integer.parseInt(indexPage);
            AsmDAL dao = new AsmDAL();
            int count = dao.getTotalAssignment();
            int endP = count / 5;
            if (count % 5 != 0) {
                endP++;
            }
            List<Assignment> asmList = dao.pagingAssignment(index);
            request.setAttribute("countA", count);
            request.setAttribute("endP", endP);
            request.setAttribute("asmList", asmList);
            request.setAttribute("tag", index);
            request.getRequestDispatcher("view/AssignmentList.jsp").forward(request, response);

        } catch (Exception e) {
            System.out.println(e);
        }

    }

