    /** 
     * Handles the HTTP <code>POST</code> method.
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
    throws ServletException, IOException {
        response.setContentType("text/html;charset=UTF-8");
        String username = request.getParameter("username");
        String password = request.getParameter("password");
        String roletxt = request.getParameter("role");
//        boolean status = Boolean.getBoolean(request.getParameter("status"));
        String fullname = request.getParameter("fullname");
        String dob = request.getParameter("dob");
        String gender = request.getParameter("gender");
        String email = request.getParameter("email");
        String mobile = request.getParameter("mobile");
        String enroldate = request.getParameter("enroldate");
        int role = 0;
        if(roletxt.equals("Trainer")){
            role = 2;
        }else if (roletxt == "Subject Manager"){
            role = 3;
        }else if (roletxt == "Administrator"){
            role =4;
        }else {
            role = 1;
        }
        
        UserDAL dao = new UserDAL();
        dao.insertUser(username,email, password, role, fullname, dob, gender, mobile, enroldate);
        response.sendRedirect("ManagementUser");
    }

